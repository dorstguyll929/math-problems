#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main() {
    int x = 5;
    float y = 3.14;
    double z = 2.71828;

    printf("The sum of x and y is %f\n", x + y);
    printf("The product of x and y is %f\n", x * y);
    printf("The square root of x is %f\n", sqrt(x));
    printf("The cube root of z is %f\n", cbrt(z));

    return 0;
}
